  const startY = useRef(null);

  function onPointerDown(e) { startY.current = e.clientY; }

  function onPointerUp(e) {
    if (startY.current !== null) {
      const dy = startY.current - e.clientY;
      if (dy > 40) onSwipeUp?.();
    }
    startY.current = null;
  }

  function onWheel(e) {
    if (e.deltaY < -8) onSwipeUp?.();
  }

